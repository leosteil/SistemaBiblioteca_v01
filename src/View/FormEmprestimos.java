    /*Atualiza a tabela na primeira vez que se abre o form*/
    /*Retorna todos os elementos*/
    public void atualizaTabela() throws SQLException {
        listResultadoConsultas.clear();
        listResultadoConsultas.addAll(daoEmprestimos.buscarTodos());
        int linha = listResultadoConsultas.size() - 1; // retorna o numero de linhas
        if (linha >= 0) {
            tblResultadoConsultas.setRowSelectionInterval(linha, linha);
            tblResultadoConsultas.scrollRectToVisible(
                    tblResultadoConsultas.getCellRect(linha, linha, true));
        }
    }

    /*Atualiza a tabela a partir de um parametro de busca*/
    /*Retorna apenas os elementos solicitados com a busca*/
    public void atualizaTabela2() throws SQLException {
        listResultadoConsultas.clear();
        listResultadoConsultas.addAll(daoEmprestimos.buscar());
        int linha = listResultadoConsultas.size() - 1; // retorna o numero de linhas
        if (linha >= 0) {
            tblResultadoConsultas.setRowSelectionInterval(linha, linha);
            tblResultadoConsultas.scrollRectToVisible(
                    tblResultadoConsultas.getCellRect(linha, linha, true));
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum emprestimo encontrado");
        }
    }


    /*Função usada para a devolução do livro*/
    /*Faz todas as verificações com relação a data de entrega do livro*/
    /*Verifica se será aplicada alguma multa ao cliente*/
    private void btnDevolverLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverLivroActionPerformed
        int linhaSelecionada = tblResultadoConsultas.getSelectedRow();
        emprestimo = listResultadoConsultas.get(linhaSelecionada);
        Livro l = emprestimo.getLivro();
        Usuario u = emprestimo.getUser();
        
        System.out.println("Dentro do devolver livro : " + u.getLogin());
        System.out.println("Nome do usuario dentro do devolver : :" + u.getNome());
        System.out.println("Outra tentativa : " + emprestimo.getUser().getLogin());
        
        java.util.Date dataRecebimento = new java.util.Date(); //data em que o livro é devolvido
        java.sql.Date dtRecebimento = new java.sql.Date(dataRecebimento.getTime()); //conversão de datas
        int multa = negocio.controlaMulta(emprestimo, (java.sql.Date) dtRecebimento);

        if (multa == 0) {
            l.setEmprestado(false);
            u.setN_exemplares(u.getN_exemplares() - 1);
        } else {
            u.setMulta(u.getMulta() + multa);
            l.setEmprestado(false);
            u.setN_exemplares(u.getN_exemplares() - 1);
        }

        daoLivros.alterar(l);
        daoUser.alterar(u);
        daoEmprestimos.excluir(emprestimo);
        try {
            atualizaTabela();
        } catch (SQLException ex) {
            Logger.getLogger(FormEmprestimos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDevolverLivroActionPerformed

    /*Função usada para a renovação dos emprestimos*/
    private void btnRenovarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRenovarActionPerformed
        int linhaSelecionada = tblResultadoConsultas.getSelectedRow();
        emprestimo = listResultadoConsultas.get(linhaSelecionada);
        Livro l = emprestimo.getLivro();
        Usuario u = emprestimo.getUser();
        java.util.Date dataRecebimento = new java.util.Date();
        java.sql.Date dtRecebimento = new java.sql.Date(dataRecebimento.getTime());

        if (negocio.verificaAtraso(emprestimo, dtRecebimento) && negocio.verificaReserva(emprestimo.getLivro())) {
            Date dt_entrega = negocio.dataEntrega(u);
            emprestimo.setDt_entrega((java.sql.Date) dt_entrega);
            daoEmprestimos.alterar(emprestimo);
        } else {
            JOptionPane.showMessageDialog(this, "Renovação não efetuada");
        }

        try {
            atualizaTabela();
        } catch (SQLException ex) {
            Logger.getLogger(FormEmprestimos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnRenovarActionPerformed


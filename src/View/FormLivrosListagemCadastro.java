    public boolean validaCampos() {
        if (!(txtISBN.getText().length() > 0)) {
            JOptionPane.showMessageDialog(this, "Informe o ISBN do livro");
            txtISBN.requestFocus();
            return false;
        }

        if (!(txtTitulo.getText().length() > 0)) {
            JOptionPane.showMessageDialog(this, "Informe o título do livro");
            txtTitulo.requestFocus();
            return false;
        }

        if (!(txtEditora.getText().length() > 0)) {
            JOptionPane.showMessageDialog(this, "Informe a editora do livro");
            txtEditora.requestFocus();
            return false;
        }

        if (!(txtEdicao.getText().length() > 0)) {
            JOptionPane.showMessageDialog(this, "Informe a edição do livro");
            txtEdicao.requestFocus();
            return false;
        }

        if (!(txtAutores.getText().length() > 0)) {
            JOptionPane.showMessageDialog(this, "Informe os autores do livro");
            txtAutores.requestFocus();
            return false;
        }

        if (!(txtAno.getText().length() > 0)) {
            JOptionPane.showMessageDialog(this, "Informe o ano do livro");
            txtAno.requestFocus();
            return false;
        }

        return true;
    }


    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int opcao
                = JOptionPane.showOptionDialog(null,
                        "Confirma a exclusão?",
                        "Pergunta",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null,
                        new String[]{"Sim", "Não"}, "Sim");
        int nLinhas = tblObjetos.getRowCount();
        if (opcao == 0 && nLinhas > 0) {

            int linhaSelecionada = tblObjetos.getSelectedRow();
            Livro obj = lstObjetos.get(linhaSelecionada);
            if(obj.getEmprestado() == true || obj.getReservado() == true){
                JOptionPane.showMessageDialog(this, "Este livro está emprestado ou reservado\n"
                        + "Primeiro cancele a reserva ou faça a devolução do livro");
            }else daoLivros.excluir(obj);
            
            try {
                atualizaTabela();
            } catch (SQLException ex) {
                Logger.getLogger(FormLivrosListagemCadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validaCampos()) {
            int linhaSelecionada = tblObjetos.getSelectedRow();
            Livro obj = lstObjetos.get(linhaSelecionada);
            if(obj.getId_livro() == null){
                daoLivros.inserir(obj);
                trataEdicao(false);
            }else daoLivros.alterar(obj);
                trataEdicao(false);
            try {
                atualizaTabela();
            } catch (SQLException ex) {
                Logger.getLogger(FormLivrosListagemCadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnSalvarActionPerformed
    }


    public void atualizaTabela() throws SQLException {
        lstObjetos.clear();
        lstObjetos.addAll(daoLivros.buscarTodos());
        int linha = lstObjetos.size() - 1; // retorna o numero de linhas
        if (linha >= 0) {
            tblObjetos.setRowSelectionInterval(linha, linha);
            tblObjetos.scrollRectToVisible(
                    tblObjetos.getCellRect(linha, linha, true));
        }
    }

